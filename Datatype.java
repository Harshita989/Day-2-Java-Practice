package Basics.Day2;

public class Datatype {
    public static void main(String[] args) {
        byte b = 2;
        System.out.println(b);
        char c = 'H';
        System.out.println(c);
        int i = 10;
        System.out.println(i);
        String s = "Harshita";
        System.out.println(s);
    }
}
